function escapeJsdocLine(s: string): string {
  return s.replace(/\*\//g, '* /').replace(/^\s*\*\s?/gm, '* ')
}

/** Infer a stable function name from the title (e.g. "Implement debounce(...)") or fall back to question id. */
export function extractMockFunctionName(title: string, id: string): string {
  const backtick = title.match(/`([a-zA-Z0-9_]+)`/)
  if (backtick) {
    const n = backtick[1]
    return RESERVED.has(n) ? `${n}_fn` : n
  }
  const implement = title.match(/^Implement\s+`?([a-zA-Z0-9_]+)`?/i)
  if (implement) {
    const n = implement[1]
    return RESERVED.has(n) ? `${n}_fn` : n
  }
  let slug = id.replace(/[^a-zA-Z0-9_]/g, '_')
  if (/^\d/.test(slug)) slug = `_${slug}`
  if (!slug) slug = 'solution'
  if (RESERVED.has(slug)) return `${slug}_fn`
  return slug
}

function pascalCase(name: string): string {
  const parts = name.split(/[^a-zA-Z0-9]+/).filter(Boolean)
  if (parts.length === 0) return 'Solution'
  return parts.map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()).join('')
}

/** Safe basename for a virtual .tsx file (Monaco model URI). */
export function questionTsxFileBase(id: string): string {
  let s = id.replace(/[^a-zA-Z0-9_-]/g, '_').replace(/_+/g, '_')
  if (/^\d/.test(s)) s = `_${s}`
  return s || 'solution'
}

export function questionTsxFileName(q: Question): string {
  return `${questionTsxFileBase(q.id)}.tsx`
}

/**
 * Ambient declarations for this card — completions for INTERVIEW_PROBLEM.* in the editor.
 */
export function buildQuestionAmbientTypes(q: Question): string {
  return `
/** Typed metadata for the active interview question (autogenerated). */
declare const INTERVIEW_PROBLEM: {
  readonly id: ${JSON.stringify(q.id)}
  readonly title: ${JSON.stringify(q.title)}
  readonly category: ${JSON.stringify(q.category)}
  readonly difficulty: ${JSON.stringify(q.difficulty)}
  readonly description: ${JSON.stringify(q.description)}
}
`
}

/**
 * Initial VS Code buffer for mock code review: problem text in JSDoc, a types interface, and a function stub.
 */
export function buildMockCodeReviewStarter(q: Question): string {
  const fnName = extractMockFunctionName(q.title, q.id)
  const typesName = `${pascalCase(fnName)}Types`

  const title = escapeJsdocLine(q.title.trim())
  const descLines = q.description
    .trim()
    .split('\n')
    .map((line) => ` * ${escapeJsdocLine(line)}`)
    .join('\n')

  return `/**
 * ${title}
 *
${descLines}
 *
 * ${q.category} · ${q.difficulty}
 *
 * Tip: \`INTERVIEW_PROBLEM\` is typed from this question (see completions).
 */

/** Shape the data / API for this problem — refine to match the prompt. */
interface ${typesName} {
  // e.g. argument types, return type helpers, nested structures
}

function ${fnName}(/* parameters — align with ${typesName} and the problem above */): unknown {
  // Your implementation
  // Example with TSX (this file is ${questionTsxFileName(q)}):
  // return <div>{INTERVIEW_PROBLEM.title}</div>
}
`
}
